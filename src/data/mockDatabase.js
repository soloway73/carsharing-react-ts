function assignRandomImageURL(cars) {
  const images = ["car1.webp", "car2.webp", "car3.webp", "car4.webp"];

  return cars.map((car) => ({
    ...car,
    imageURL: images[Math.floor(Math.random() * images.length)],
  }));
}

mockDatabase.cars = assignRandomImageURL(mockDatabase.cars);
const getAllCars = () => mockDatabase.cars;

// Фильтрация
const carsInCity = (cityId) =>
  mockDatabase.cars.filter((car) => car.cityId === cityId);
const filterCities = (request) => {
  return mockDatabase.cities
    .map((city) => city.name)
    .filter((city) => city.name.toLowerCase().includes(request.toLowerCase()));
};
const filterLocations = (request) => {
  return mockDatabase.locations
    .map((location) => location.name)
    .filter((location) =>
      location.toLowerCase().includes(request.toLowerCase())
    );
};

export default {
  mockDatabase,
  carsInCity,
  filterCities,
  filterLocations,
  getAllCars,
};
